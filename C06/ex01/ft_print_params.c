#include <unistd.h>

int	main(int ac, char **av)
{
	int	n;
	int	i;

	n = 0;
	i = 1;
	while (i < ac)
	{
		n = 0;
		while (av[i][n] != '\0')
		{
			write (1, &av[i][n], 1);
			n++;
		}
		write (1, "\n", 1);
		i++;
	}
	return (0);
}
