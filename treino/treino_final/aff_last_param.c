#include <unistd.h>

int	main(int ac, char **av)
{
	int	n;

	if (ac < 2)
	{
		write (1, "\n", 1);
		return (0);
	}
	n = 0;
	while (av[ac - 1][n] != '\0')
	{
		write (1, &av[ac - 1][n], 1);
		n++;
	}
	write (1, "\n", 1);
	return (0);
}
