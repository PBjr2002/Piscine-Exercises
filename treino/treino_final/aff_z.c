#include <unistd.h>

int	main(int ac, char **av)
{
	int	n;

	if (ac != 2)
	{
		write (1, "z\n", 2);
		return (0);
	}
	n = 0;
	while (av[1][n] != '\0')
	{
		if (av[1][n] == 'z')
		{
			write (1, &av[1][n], 1);
			write (1, "\n", 1);
			return (0);
		}
		n++;
	}
	write (1, "z\n", 2);
	return (0);
}
