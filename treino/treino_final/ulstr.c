#include <unistd.h>

int	main(int ac, char **av)
{
	int	n;

	if (ac != 2)
	{
		write (1, "\n", 1);
		return (0);
	}
	n = 0;
	while (av[1][n] != '\0')
	{
		if (av[1][n] >= 'a' && av[1][n] <= 'z')
			av[1][n] = av[1][n] - 32;
		else if (av[1][n] >= 'A' && av[1][n] <= 'Z')
			av[1][n] = av[1][n] + 32;
		n++;
	}
	n = 0;
	while (av[1][n] != '\0')
	{
		write (1, &av[1][n], 1);
		n++;
	}
	write (1, "\n", 1);
	return (0);
}
