#include <unistd.h>

int	main(int ac, char **av)
{
	int	n;

	n = 0;
	if (av[1][n] == '\0' || ac != 2)
	{
		write(1, "\n", 1);
		return (0);
	}
	while (av[1][n] == ' ' || av[1][n] == '\t')
	{
		n++;
	}
	while (av[1][n] != '\0')
	{
		if (av[1][n] == ' ' || av[1][n] == '\t')
		{
			return (0);
		}
		write (1, &av[1][n], 1);
		n++;
	}
	return (0);
}
